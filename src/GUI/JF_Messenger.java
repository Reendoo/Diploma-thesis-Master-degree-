    public DefaultTableModel getSpravy() {
        DefaultTableModel mod = null;
        mod = new DefaultTableModel() {
            public Class<?> getColumnClass(int column) {
//                    if (column == 0 || column == 1 || column == 2 || column == 3) {
//                        return Integer.class;
//                    } else if (column == (columnCount - 2)) {
//                        return Boolean.class;
//                    }
                return Object.class;
            }
            boolean[] canEdit = new boolean[]{
                false, false, false};

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        };
        Object[] header = new Object[3];
        header[0] = "Dátum prijatia";
        header[1] = "Odosielateľ";
        header[2] = "Správa";
        mod.setColumnIdentifiers(header);
        SimpleDateFormat simpleDateFormat2 = new SimpleDateFormat("dd.MM.yyyy HH:mm:ss");
        for (PrijataSprava prijataSprava : oh.getNespracovaneSpravy()) {
            Object[] data = {simpleDateFormat2.format(prijataSprava.getDatum()), prijataSprava.getTel(), prijataSprava.getText()};
            mod.addRow(data);
        }

        return mod;
    }

