    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (odpovedam == false) {
            int row = jTable1.rowAtPoint(evt.getPoint());
            SimpleDateFormat simpleDateFormat2 = new SimpleDateFormat("dd.MM.yyyy HH:mm:ss");
            jLabel1.setText("Dátum: " + simpleDateFormat2.format(oh.getNespracovaneSpravy().get(row).getDatum()));
            jLabel2.setText("Odosielateľ: " + oh.getNespracovaneSpravy().get(row).getTel());
            jLabel3.setText("" + row);
            jTextPane1.setText(oh.getNespracovaneSpravy().get(row).getText());
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (jButton1.getText().equals("Vymaž správu")) {
                int index = Integer.parseInt(jLabel3.getText());
                if (index < 0) {
                    JOptionPane.showMessageDialog(this, "Prosím, vyberte správu ktorú chcete vymazať");
                    return;
                }
                Object[] ponuka = {"Áno", " Nie"};
                int moznost = JOptionPane.showOptionDialog(this,
                        "Naozaj chcete vymazať správu:\n"
                        + jLabel1.getText() + "\n"
                        + jLabel2.getText() + " ?",
                        "Vymazanie nespracovanej správy",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE,
                        null, //ikona
                        ponuka,
                        ponuka[0]);
                if (moznost == 0) {
                    jLabel1.setText("Dátum: ");
                    jLabel2.setText("Odosielateľ: ");
                    jLabel3.setText("-1");
                    jTextPane1.setText("");
                    oh.getNespracovaneSpravy().remove(index);
                    Refresher.r.refresNespracovaneSpravy();
                    jTable1.setModel(getSpravy());
                    jTable1.clearSelection();
                }
            } else {
                jButton1.setText("Vymaž správu");
                jButton2.setText("Odpovedať");
                jTable1.setEnabled(true);
                jLabel1.setText("Dátum: ");
                jLabel2.setText("Odosielateľ: ");
                jLabel3.setText("-1");
                jTextPane1.setText("");
                jTable1.clearSelection();
                odpovedam = false;
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jButton2.getText().equals("Odpovedať")) {
            if (Integer.parseInt(jLabel3.getText()) < 0) {
                JOptionPane.showMessageDialog(this, "Prosím, vyberte správu na ktorú chcete odpovedať");
                return;
            }
            jButton2.setText("Odoslať");
            jButton1.setText("Späť");
            jTable1.setEnabled(false);
            jTextPane1.setText("Sem napíšte odpoveď ...");
            odpovedam = true;
        } else if (jButton2.getText().equals("Odoslať")) {
            if (Integer.parseInt(jLabel3.getText()) < 0) {
                JOptionPane.showMessageDialog(this, "Prosím, vyberte správu na ktorú chcete odpovedať");
                return;
            }
            String text = jTextPane1.getText();
            if (text.startsWith("Sem napíšte odpoveď")) {
                JOptionPane.showMessageDialog(this, "Prosím zadajte text správy");
                return;
            }
            int index = Integer.parseInt(jLabel3.getText());
            ArrayList<String> prijimatelia = new ArrayList<>();
            prijimatelia.add(oh.getNespracovaneSpravy().get(index).getTel());
            String txt = oh.getMsg().posliSMS(new SMS(text, "OHRajec", prijimatelia));
            if (txt.equals("problem")) {                
                JOptionPane.showMessageDialog(this, "Nepodarilo sa pripojiť k sms bráne");
                return;
            } else if (txt.startsWith("OK")) {
                JOptionPane.showMessageDialog(this, "Úspešne ste odpovedali na sms");
            } else {
                JOptionPane.showMessageDialog(this, txt);
                return;
            }
            jButton2.setText("Odpovedať");
            jButton1.setText("Vymaž správu");
            odpovedam = false;
            Object[] ponuka = {"Áno", " Nie"};
            int moznost = JOptionPane.showOptionDialog(this,
                    "Naozaj chcete vymazať správu:\n"
                    + jLabel1.getText() + "\n"
                    + jLabel2.getText() + " ?",
                    "Vymazanie nespracovanej správy",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null, //ikona
                    ponuka,
                    ponuka[0]);
            if (moznost == 0) {
                jLabel1.setText("Dátum: ");
                jLabel2.setText("Odosielateľ: ");
                jLabel3.setText("-1");
                jTextPane1.setText("");
                oh.getNespracovaneSpravy().remove(index);
                Refresher.r.refresNespracovaneSpravy();
                jTable1.setModel(getSpravy());
            }
            jTable1.clearSelection();
            jTable1.setEnabled(true);

        }


    }//GEN-LAST:event_jButton2ActionPerformed

